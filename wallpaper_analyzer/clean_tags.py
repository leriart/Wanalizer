def _print_pollution_report(polluted: Set[str], df: Dict[str, int], n: int) -> None:
    if not polluted:
        print(f"No polluted tags detected across {n} categories.")
        return
    print(f"Detected {len(polluted)} polluted tag(s) (present in >=50% of categories):")
    print()
    print(f"  {'tag':<22s} {'df':>4s} / {n}  examples")
    print(f"  {'-'*22}  {'-'*7}  {'-'*30}")
    for t in sorted(polluted, key=lambda x: -df[x]):
        df_t = df[t]
        # Find 3 categories that use this tag for examples.
        examples = []
        for cat in c.CATEGORIES:
            cfg = c.get_category_config(cat)
            if cfg and t in [str(x).lower() for x in (cfg.get("tags") or [])]:
                examples.append(cat)
                if len(examples) >= 3:
                    break
        example_str = ", ".join(examples[:3])
        if len(examples) > 3:
            example_str += f", ... (+{len(examples) - 3})"
        print(f"  {t:<22s} {df_t:>4d}  {example_str}")


def main(argv: Optional[List[str]] = None) -> int:
    """Entry point: `python -m wallpaper_analyzer.clean_tags [args]`.

    Use `--analyse` (default) to just report polluted tags.
    Use `--clean` to strip them (with a backup)."""
    import argparse
    p = argparse.ArgumentParser(
        prog="python -m wallpaper_analyzer.clean_tags",
        description="Detect and clean polluted tags in the category registry.",
    )
    p.add_argument("--analyse", action="store_true", default=True,
                   help="Analyse and report polluted tags (default).")
    p.add_argument("--clean", action="store_true",
                   help="Strip polluted tags from every category (writes .bak files).")
    p.add_argument("--dry-run", action="store_true",
                   help="With --clean, just print what would be removed.")
    p.add_argument("--threshold", type=float, default=DEFAULT_POLLUTED_THRESHOLD,
                   help="Pollution threshold (fraction of categories).")
    p.add_argument("--max-tags", type=int, default=DEFAULT_MAX_TAGS_PER_CATEGORY,
                   help="Cap each category at this many tags after cleaning.")
    p.add_argument("--categories-dir", default=None,
                   help="Path to the categories directory (default: settings.dest_dir).")
    args = p.parse_args(argv)

    target_dir = args.categories_dir or c.CATEGORIES_DIR
    if not target_dir:
        print("ERROR: no categories directory (set in .wallpaper_analyzer.json or pass --categories-dir)")
        return 1

    if not args.clean:
        polluted, df = analyse_pollution(target_dir)
        if not c.CATEGORIES:
            c.discover_categories(target_dir)
        n = len(c.CATEGORIES)
        _print_pollution_report(polluted, df, n)
        return 0

    # Clean path
    summary = clean_category_tags(
        categories_dir=target_dir,
        polluted_threshold=args.threshold,
        max_tags_per_category=args.max_tags,
        backup=True,
        dry_run=args.dry_run,
    )
    if args.dry_run:
        print(f"[dry-run] Would remove polluted tags from {len(summary)} categories:")
    else:
        print(f"Cleaned {len(summary)} categories:")
    total_removed = 0
    for cat, info in summary.items():
        if info["removed"]:
            print(f"  {cat:12s}: {info['before']:3d} -> {info['kept']:3d}  "
                  f"({len(info['removed'])} removed: {', '.join(info['removed'][:6])}"
                  f"{' ...' if len(info['removed']) > 6 else ''})")
            total_removed += len(info["removed"])
    print(f"\nTotal tags removed: {total_removed}")
    if args.dry_run:
        print("Nothing was written. Re-run without --dry-run to apply.")
    else:
        print("Backups were saved next to each .category.json as .bak")
    return 0


if __name__ == "__main__":
    import sys
    sys.exit(main(sys.argv[1:]))
